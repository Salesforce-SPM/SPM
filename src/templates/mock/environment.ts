import { Environment } from '../../controller/environment';

export function invalid() {
    const mock = new Environment();
    mock.name = '';
    mock.url = "salesforce.com";
    mock.user = "invalidUser";
    mock.password = '';
    mock.token = 'pWEqYJyozgaNcIt';

    return mock;
}

export function valid() {
    const mock = new Environment();
    // mock.name = 'Test env';
    mock.name = Math.random().toString(36).replace(/[^a-z]+/g, '').substr(0, 5);
    mock.url = "https://test.salesforce.com";
    mock.user = "test@fake-user.com";
    mock.password = 'SomePassWord';
    mock.token = 'RuBy744682pWEqYJyozgaNcIt';

    return mock;
}
